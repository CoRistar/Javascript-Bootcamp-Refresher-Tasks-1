// Exercise 1: Numbers

let integerNumber = 62;
console.log("Integer:", integerNumber);

let floatNumber = 6.24;
console.log("Float:", floatNumber);

let addition = integerNumber + 2;
console.log("Addition:", addition);

let subtraction = integerNumber - 2;
console.log("Subtraction:", subtraction);

let multiplication = integerNumber * 2;
console.log("Multiplication:", multiplication);

let division = integerNumber / 2;
console.log("Division:", division);

let modulus = integerNumber % 2;
console.log("Modulus:", modulus);

let exponentiation = integerNumber ** 2;
console.log("Exponentiation:", exponentiation);


// Exercise 2: Boolean and Operators

let x = 8;
let y = 12;

let isGreater = x > y;
console.log("x > y:", isGreater);

let isLessOrEqual = x <= y;
console.log("x <= y:", isLessOrEqual);

let isEqual = x == y;
console.log("x == y:", isEqual);

let isNotEqual = x != y;
console.log("x != y:", isNotEqual);

let a = true;
let b = false;

let andOperation = a && b;
console.log("a AND b:", andOperation);

let orOperation = a || b;
console.log("a OR b:", orOperation);

let notA = !a;
console.log("NOT a:", notA);


